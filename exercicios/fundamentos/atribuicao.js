//Operadores de atribuição

const a = 7 // 7 está sendo atribuido a A
let b = 3 // 3 está sendo atribuido a b

b += a // atribuição adtiva => Equivale a b = b + a 
console.log(b)

b -= 4 // b = b - 4 
console.log(b)

b *= 2 // b = b * 2 
console.log(b)

b /= 2 // b = b / 2 
console.log(b)

b %= 2 // b = b % 2
console.log(b)